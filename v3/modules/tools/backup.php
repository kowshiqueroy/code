// ── Topology sort: parents before children ───────────────────
function sortTablesByDependency(array $tables): array {
    // Build FK dependency map from information_schema
    $deps = array_fill_keys($tables, []);
    $fkRows = dbFetchAll(
        "SELECT TABLE_NAME, REFERENCED_TABLE_NAME
         FROM information_schema.KEY_COLUMN_USAGE
         WHERE TABLE_SCHEMA = DATABASE()
           AND REFERENCED_TABLE_NAME IS NOT NULL"
    );
    foreach ($fkRows as $r) {
        $t = $r['TABLE_NAME'];
        $ref = $r['REFERENCED_TABLE_NAME'];
        if (isset($deps[$t]) && $ref !== $t) {
            $deps[$t][] = $ref;
        }
    }

    $sorted  = [];
    $visited = [];

    function topoVisit(string $table, array &$deps, array &$visited, array &$sorted): void {
        if (isset($visited[$table])) return;
        $visited[$table] = true;
        foreach ($deps[$table] ?? [] as $dep) {
            topoVisit($dep, $deps, $visited, $sorted);
        }
        $sorted[] = $table;
    }

    foreach ($tables as $t) {
        topoVisit($t, $deps, $visited, $sorted);
    }
    return $sorted;
}

// ── Pure-PHP SQL dump ────────────────────────────────────────
function generateSqlDump(): string {
    $out   = [];
    $out[] = '-- ============================================================';
    $out[] = '-- SQL Backup generated by ' . APP_NAME;
    $out[] = '-- Date    : ' . date('Y-m-d H:i:s');
    $out[] = '-- ============================================================';
    $out[] = '';
    $out[] = 'SET FOREIGN_KEY_CHECKS=0;';
    $out[] = 'SET SQL_MODE="NO_AUTO_VALUE_ON_ZERO";';
    $out[] = 'SET time_zone="+00:00";';
    $out[] = '';

    // Get all tables then sort parents-first
    $rawTables = array_map(fn($r) => reset($r), dbFetchAll('SHOW TABLES'));
    $tables    = sortTablesByDependency($rawTables);

    // DROP in reverse order (children first) to avoid FK conflicts
    foreach (array_reverse($tables) as $table) {
        $out[] = "DROP TABLE IF EXISTS `$table`;";
    }
    $out[] = '';

    foreach ($tables as $table) {
        $createRow = dbFetch("SHOW CREATE TABLE `$table`");
        $createSql = $createRow['Create Table'] ?? '';

        $out[] = "-- --------------------------------------------------------";
        $out[] = "-- Table: `$table`";
        $out[] = "-- --------------------------------------------------------";
        $out[] = $createSql . ';';
        $out[] = '';

        $countRow = dbFetch("SELECT COUNT(*) AS cnt FROM `$table`");
        if ((int)($countRow['cnt'] ?? 0) === 0) continue;

        $chunkSize = 500;
        $offset    = 0;
        while (true) {
            $rows = dbFetchAll("SELECT * FROM `$table` LIMIT $chunkSize OFFSET $offset");
            if (!$rows) break;

            $cols = '`' . implode('`, `', array_keys($rows[0])) . '`';
            $valueGroups = [];
            foreach ($rows as $r) {
                $vals = array_map(function ($v) {
                    if ($v === null)    return 'NULL';
                    if (is_numeric($v)) return $v;
                    return "'" . addslashes((string)$v) . "'";
                }, array_values($r));
                $valueGroups[] = '(' . implode(', ', $vals) . ')';
            }
            foreach (array_chunk($valueGroups, 100) as $batch) {
                $out[] = "INSERT INTO `$table` ($cols) VALUES";
                $out[] = implode(",\n", $batch) . ';';
            }
            $offset += $chunkSize;
            if (count($rows) < $chunkSize) break;
        }
        $out[] = '';
    }

    $out[] = 'SET FOREIGN_KEY_CHECKS=1;';
    return implode("\n", $out);
}

// ── SQL statement splitter ────────────────────────────────────
// Character-level parser: tracks quoted strings so a semicolon
// inside a value or multiline CREATE TABLE body won't split early.
function splitSqlStatements(string $sql): array {
    $statements = [];
    $len        = strlen($sql);
    $i          = 0;
    $current    = '';
    $inSingle   = false;
    $inDouble   = false;

    while ($i < $len) {
        $ch   = $sql[$i];
        $next = $sql[$i + 1] ?? '';

        // line comment -- skip to EOL
        if (!$inSingle && !$inDouble && $ch === '-' && $next === '-') {
            while ($i < $len && $sql[$i] !== "\n") $i++;
            $i++;
            continue;
        }

        // single-quote toggle (handle escaped '' )
        if (!$inDouble && $ch === "'") {
            if ($inSingle && $next === "'") {
                $current .= "''";
                $i += 2;
                continue;
            }
            $inSingle = !$inSingle;
            $current .= $ch;
            $i++;
            continue;
        }

        // double-quote toggle
        if (!$inSingle && $ch === '"') {
            if ($inDouble && $next === '"') {
                $current .= '""';
                $i += 2;
                continue;
            }
            $inDouble = !$inDouble;
            $current .= $ch;
            $i++;
            continue;
        }

        // backslash escape inside quotes
        if (($inSingle || $inDouble) && $ch === '\\') {
            $current .= $ch . $next;
            $i += 2;
            continue;
        }

        // statement terminator (only outside quotes)
        if (!$inSingle && !$inDouble && $ch === ';') {
            $stmt = trim($current);
            if ($stmt !== '') {
                $statements[] = $stmt;
            }
            $current = '';
            $i++;
            continue;
        }

        $current .= $ch;
        $i++;
    }

    $stmt = trim($current);
    if ($stmt !== '') {
        $statements[] = $stmt;
    }

    return $statements;
}

