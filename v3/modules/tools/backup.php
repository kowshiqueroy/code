if ($action === 'restore_db') {
    // CSRF-style token check
    if (
        empty($_POST['confirm_token']) ||
        $_POST['confirm_token'] !== ($_SESSION['restore_token'] ?? '')
    ) {
        flash('error', 'Invalid confirmation token. Please try again.');
        redirect('backup');
    }
    unset($_SESSION['restore_token']);

    if (empty($_FILES['sql_file']['tmp_name'])) {
        flash('error', 'No file uploaded.');
        redirect('backup');
    }

    $origName = $_FILES['sql_file']['name'] ?? '';
    if (strtolower(pathinfo($origName, PATHINFO_EXTENSION)) !== 'sql') {
        flash('error', 'Only .sql files are accepted.');
        redirect('backup');
    }

    $sql = file_get_contents($_FILES['sql_file']['tmp_name']);
    if (!$sql) {
        flash('error', 'Could not read the uploaded file.');
        redirect('backup');
    }

    // Split into individual statements (character-level parser handles
    // multi-line CREATE TABLE, semicolons inside quoted values, etc.)
    $statements = splitSqlStatements($sql);

    if (empty($statements)) {
        flash('error', 'No valid SQL statements found in the uploaded file.');
        redirect('backup');
    }

    $errors  = [];
    $count   = 0;

    // Force FK checks off for the whole restore session
    try { dbQuery('SET FOREIGN_KEY_CHECKS=0'); } catch (Throwable $e) {}
    try { dbQuery('SET SQL_MODE="NO_AUTO_VALUE_ON_ZERO"'); } catch (Throwable $e) {}

    foreach ($statements as $idx => $stmt) {
        $stmt = trim($stmt);
        if ($stmt === '') continue;

        // Skip SET statements we already ran or that PDO can't handle
        if (preg_match('/^SET\s+(FOREIGN_KEY_CHECKS|SQL_MODE|time_zone)/i', $stmt)) {
            continue;
        }

        try {
            dbQuery($stmt);
            $count++;
        } catch (Throwable $e) {
            $errors[] = [
                'n'    => $idx + 1,
                'sql'  => substr($stmt, 0, 120),
                'err'  => $e->getMessage(),
            ];
        }
    }

    try { dbQuery('SET FOREIGN_KEY_CHECKS=1'); } catch (Throwable $e) {}

    logAction(
        'IMPORT', 'backup', null,
        "Admin restored DB from: $origName ($count statements, " . count($errors) . " errors)"
    );

    if ($errors) {
        // Store errors in session so we can display them on the page
        $_SESSION['restore_errors'] = $errors;
        flash('warning', count($errors) . ' error(s) during restore. ' . $count . ' statements ran. See details below.');
    } else {
        flash('success', "Restore complete — $count statements executed successfully.");
    }
    redirect('backup');
}

